#!/usr/bin/env python3
"""forge-wiki `fw` — mechanical I/O layer for an agent-facing markdown wiki.

Design doctrine (FH, 2026-07-18):
- The wiki's CONTENT is curated (human/HITL-gated agent writes). The wiki's
  RECENCY INDEX is a derivation, never state — so index conflicts are healed
  by regeneration, not by hand-merge.
- Robust-by-construction over instructed-care: conflict markers and duplicated
  AUTO blocks inside the derived region are collapsed mechanically.
- Recency = filename date > nearest dated parent dir > mtime (rsync/copy
  inherits source mtimes; mtime alone lets stale files masquerade as fresh).
- Fail direction: `--check` exits non-zero on drift (CI-gateable); heal never
  touches the curated region (curated conflicts stay a human merge — HITL).

Subcommands:
  sync   [--write]   regenerate the AUTO-INDEX block (dry-run by default)
  lint               frontmatter report (report-only, never rewrites)
  heal   [--write]   collapse conflict markers / duplicate AUTO blocks, then sync
  doctor             freshness + stale-pointer + closed-but-pointed report
  check              exit 1 if AUTO block differs from a fresh regeneration
"""

import os
import re
import sys
from datetime import date
from pathlib import Path

START = "<!-- AUTO-INDEX:START (generated by fw.py — do not hand-edit inside) -->"
END = "<!-- AUTO-INDEX:END -->"
DATE_RE = re.compile(r"(20\d{2})[-_](\d{2})[-_](\d{2})")
CLOSED_RE = re.compile(r"STATUS:\s*SUPERSEDED|status:\s*(DONE|RESOLVED)", re.I)
CONFLICT_RE = re.compile(r"^(<{7}|={7}|>{7})", re.M)
_AUTOISH = (
    re.compile(r"^<!-- AUTO-INDEX:(START|END)"),
    re.compile(r"^### \S+/ — latest \d+ of \d+"),
    re.compile(r"^- `[^`]+` \(\d{4}-\d{2}-\d{2}\)"),
    re.compile(r"^> Derived — regenerate"),
    re.compile(r"^(<{7}|={7}|>{7})"),
    re.compile(r"^$"),
)


def has_conflict(text: str) -> bool:
    """F-3: a bare `=======` is also a setext underline — it only counts as a git
    conflict when both `<<<<<<<` and `>>>>>>>` marker lines are present too."""
    return bool(re.search(r"^<{7}( |$)", text, re.M) and re.search(r"^>{7}( |$)", text, re.M))
PER_SECTION = int(os.environ.get("FW_PER_SECTION", "5"))
EXCLUDE_DIRS = {".git", ".github", "backups", "node_modules", "bin", "tests", "scripts"}


def home_file(root: Path) -> Path:
    """The wiki home: INDEX.md, or the OKF-reserved index.md. F-4 (field-found,
    2026-07-18): on case-insensitive filesystems (macOS/Windows) the two names are
    ONE file, so fw treats first-existing as the single home — the AUTO block and
    OKF's sectioned link lists are the same shape, one file serves both roles."""
    for name in ("INDEX.md", "index.md"):
        p = root / name
        if p.exists():
            return p
    return root / "INDEX.md"


def wiki_root() -> Path:
    p = Path.cwd()
    for cand in [p, *p.parents]:
        if (cand / "INDEX.md").exists() or (cand / "index.md").exists():
            return cand
    sys.exit("fw: no INDEX.md/index.md found here or above — run inside a wiki (fw init first)")


def sections(root: Path):
    out = []
    for d in sorted(root.iterdir()):
        if d.is_dir() and d.name not in EXCLUDE_DIRS and not d.name.startswith("."):
            if any(d.rglob("*.md")):
                out.append(d.name)
    return out


def file_date(p: Path) -> str:
    m = DATE_RE.search(p.name)
    if m:
        return "-".join(m.groups())
    for parent in p.parents:
        m = DATE_RE.search(parent.name)
        if m:
            return "-".join(m.groups())
    try:
        return date.fromtimestamp(p.stat().st_mtime).isoformat()
    except OSError:
        return "0000-00-00"


def head_lines(p: Path, n: int = 40):
    try:
        with open(p, encoding="utf-8", errors="replace") as f:
            return [next(f, "") for _ in range(n)]
    except OSError:
        return []


def summary_of(lines) -> str:
    if lines and lines[0].strip() == "---":
        for ln in lines[1:]:
            if ln.strip() == "---":
                break
            m = re.match(r"\s*description:\s*[\"']?(.+?)[\"']?\s*$", ln)
            if m:
                return m.group(1)
    for ln in lines:
        if ln.startswith("#"):
            return ln.lstrip("# ").strip()
    return ""


def lint_frontmatter(lines) -> list:
    if not lines or lines[0].strip() != "---":
        return ["no-frontmatter"]
    closing = next((i for i, ln in enumerate(lines[1:], 1) if ln.strip() == "---"), None)
    if closing is None:
        return ["unclosed-frontmatter(first-40-lines)"]
    issues = []
    body = lines[1:closing]
    keys = [m.group(1) for ln in body if not ln.startswith((" ", "\t", "#"))
            for m in [re.match(r"([A-Za-z_][\w-]*):", ln)] if m]
    if len(keys) != len(set(keys)):
        issues.append("duplicate-keys")
    if not any(k in ("description", "name", "title", "type") for k in keys):
        issues.append("no-identity-field")
    return issues


def collect(root: Path, section: str):
    files = [p for p in (root / section).rglob("*.md")
             if ".git" not in p.parts and p.name != "INDEX.md"]
    files.sort(key=lambda p: (file_date(p), p.name), reverse=True)
    return files


def build_block(root: Path) -> str:
    out = [START,
           f"> Derived — regenerate with `fw sync --write`; conflicts here heal by regeneration"
           f" (`fw heal --write`). Last sync: {date.today().isoformat()}."]
    for section in sections(root):
        files = collect(root, section)
        if not files:
            continue
        out.append(f"\n### {section}/ — latest {min(PER_SECTION, len(files))} of {len(files)}")
        for p in files[:PER_SECTION]:
            lines = head_lines(p)
            desc = summary_of(lines)
            desc = (desc[:110] + "…") if len(desc) > 110 else desc
            closed = " ⛔closed" if CLOSED_RE.search("".join(lines)) else ""
            out.append(f"- `{p.relative_to(root)}` ({file_date(p)}){closed}{' — ' + desc if desc else ''}")
    out.append(END)
    return "\n".join(out)


def write_llms_txt(root: Path):
    """llms.txt (https://llmstxt.org) — derived like the index: H1 + blockquote,
    then per-section link lists with descriptions. Regenerated on every sync."""
    lines = [f"# {root.resolve().name}", "",
             "> Agent-facing markdown wiki (forge-wiki). Read INDEX.md first; "
             "files carry OKF-compatible YAML frontmatter (type + description).", ""]
    for section in sections(root):
        files = collect(root, section)
        if not files:
            continue
        lines.append(f"## {section}/")
        lines.append("")
        for p in files[:PER_SECTION]:
            desc = summary_of(head_lines(p)) or p.stem
            lines.append(f"- [{p.stem}]({p.relative_to(root)}): {desc[:120]}")
        lines.append("")
    (root / "llms.txt").write_text("\n".join(lines), encoding="utf-8")


def split_regions(text: str):
    """(curated_before, auto_region_or_None, curated_after). Self-healing with a
    curated-inviolability bound (F-2): the FIRST-START..LAST-END span collapses
    only when every line in it is strictly auto-shaped (merge-duplicated blocks,
    conflict markers). Any other line in the span → conservative mode: only the
    LAST well-formed START..END pair is treated as the auto region; earlier
    orphan remnants stay in place for `fw doctor` to flag — fw never guesses
    which human text to delete."""
    s_first = text.find("<!-- AUTO-INDEX:START")
    e_last = text.rfind(END)
    if s_first == -1 or e_last == -1 or e_last < s_first:
        return text, None, ""
    span = text[s_first:e_last + len(END)]
    if all(any(p.match(ln.strip()) for p in _AUTOISH) for ln in span.splitlines()):
        return text[:s_first], span, text[e_last + len(END):]
    s_last = text.rfind("<!-- AUTO-INDEX:START", 0, e_last)
    if s_last == -1 or s_last > e_last:
        return text, None, ""
    return text[:s_last], text[s_last:e_last + len(END)], text[e_last + len(END):]


def regenerate(root: Path, write: bool, heal: bool = False) -> int:
    index = home_file(root)
    text = index.read_text(encoding="utf-8", errors="replace")
    before, auto, after = split_regions(text)
    warn = 0
    if auto and has_conflict(auto):
        print("[heal] conflict markers inside AUTO region — collapsed by regeneration")
    for region, name in ((before, "before"), (after, "after")):
        if has_conflict(region):
            warn += 1
            print(f"[warn] conflict markers in CURATED region ({name} AUTO block) — human merge required; fw does not touch curated content")
    block = build_block(root)
    if auto is None:
        new = text.rstrip() + "\n\n" + block + "\n"
    else:
        new = before + block + after
    if write:
        index.write_text(new, encoding="utf-8")
        write_llms_txt(root)
        print(f"[fw sync] AUTO block + llms.txt written ({len(sections(root))} sections)")
    else:
        drift = (auto or "") != block
        print(f"[fw sync] DRY-RUN — drift vs current: {'YES' if drift else 'no'} (pass --write)")
        if heal:
            return 1 if drift else 0
    return 2 if warn else 0


def cmd_lint(root: Path) -> int:
    no_fm, issues_n = 0, 0
    for section in sections(root):
        for p in collect(root, section):
            issues = lint_frontmatter(head_lines(p))
            if issues == ["no-frontmatter"]:
                no_fm += 1
            elif issues:
                issues_n += 1
                print(f"  {p.relative_to(root)}: {', '.join(issues)}")
    print(f"[fw lint] no-frontmatter: {no_fm} · structural issues: {issues_n} (report-only)")
    return 0


def cmd_doctor(root: Path) -> int:
    text = home_file(root).read_text(encoding="utf-8", errors="replace")
    before, auto, after = split_regions(text)
    curated = before + after
    broken = []
    secs = set(sections(root))
    for m in re.finditer(r"`([\w./-]+?\.md)`", curated):
        rel = m.group(1)
        # only paths rooted at THIS wiki's sections — cross-repo pointers are not ours to judge
        if "/" in rel and rel.split("/")[0] in secs and not (root / rel).exists() \
                and not any(c in rel for c in "<>{}*"):
            broken.append(rel)
    # F-2 (sim finding 2026-07-18): a lost/deleted marker leaves an orphaned old AUTO
    # body inside the curated region — check/sync then treat it as curated content.
    # Detect and REPORT only: the curated region is human-owned, removal is HITL.
    orphan = [ln.strip()[:80] for ln in curated.splitlines()
              if re.match(r"^### [\w.-]+/ — latest \d+ of \d+", ln.strip())
              or "Derived — regenerate with" in ln]
    if orphan:
        print(f"[fw doctor] ORPHAN auto-block remnant in curated region ({len(orphan)} line(s)) — "
              f"likely a lost AUTO marker; review and delete by hand (fw will not touch curated content):")
        for ln in orphan[:5]:
            print(f"  {ln}")
    m = re.search(r"Last sync: (\d{4}-\d{2}-\d{2})", auto or "")
    last_sync = m.group(1) if m else "never"
    newer = sum(1 for s in sections(root) for p in collect(root, s) if file_date(p) > last_sync)
    print(f"[fw doctor] last sync: {last_sync} · files dated after sync: {newer}")
    if broken:
        print(f"[fw doctor] {len(broken)} curated pointer(s) → missing files:")
        for b in broken[:15]:
            print(f"  {b}")
    return 1 if (broken or orphan) else 0


def cmd_check(root: Path) -> int:
    text = home_file(root).read_text(encoding="utf-8", errors="replace")
    _, auto, _ = split_regions(text)
    fresh = build_block(root)

    def norm(s):
        return re.sub(r"Last sync: \d{4}-\d{2}-\d{2}", "", s or "")
    if norm(auto) != norm(fresh):
        print("[fw check] DRIFT — AUTO block does not match regeneration (run fw sync --write)")
        return 1
    print("[fw check] clean")
    return 0


INIT_INDEX = """# {name} — Wiki Home (agents: read this first)

> INDEX-first wiki for humans AND agents. Curated pointers live above the
> AUTO-INDEX block; recency below it is derived — never hand-edit inside.

## Live pointers (curated — what is canonical, not what is recent)
- (add: "★ topic → `section/file.md` — one-line why-here")
"""

INIT_AGENTS = """<!-- FORGE-WIKI:START -->
## Wiki protocol (forge-wiki)
- Read `INDEX.md` first; open only files the index makes relevant (progressive disclosure).
- Every knowledge file: YAML frontmatter with `type` + `description` (OKF v0.1-compatible).
- Write into your OWN scoped files (e.g. `signals/<topic>_<date>_<author>.md`); never rewrite
  another author's file wholesale — propose changes via PR/handoff instead (HITL gate).
- Write content-first: push your files WITHOUT regenerating the index per write (under
  concurrent writers, per-write index commits make everyone conflict — measured). The index
  is repaired by any later `python3 bin/fw.py sync --write` (CI / session close / next reader).
  Index conflicts: `fw heal --write` — the AUTO block is a derivation, regeneration IS the merge
  resolution. Rejected push: back off with jitter and retry (a give-up stays local, republish later).
- Before research/measurement: grep the relevant section for a prior result; extend, don't fork.
<!-- FORGE-WIKI:END -->
"""


def cmd_init(root_dir: Path):
    # Phase-0 state audit (absorbed from revfactory/harness via FH autopilot doctrine):
    # detect existing wiki-shaped assets first — found → extend, never fork/overwrite.
    found = [p.name for p in (root_dir / n for n in
             ("openwiki", "wiki", "docs/wiki", ".openwiki")) if p.exists()]
    if found:
        print(f"[fw init] Phase-0: existing wiki asset(s) detected: {', '.join(found)} — "
              f"forge-wiki will coexist; consider pointing INDEX.md at them instead of duplicating")
    name = root_dir.resolve().name
    index = root_dir / "INDEX.md"
    if index.exists():
        print("[fw init] INDEX.md already exists — refusing to overwrite (non-overwriting by doctrine)")
    else:
        index.write_text(INIT_INDEX.format(name=name), encoding="utf-8")
        print("[fw init] INDEX.md created")
    agents = root_dir / "AGENTS.md"
    text = agents.read_text(encoding="utf-8") if agents.exists() else ""
    if "FORGE-WIKI:START" in text:
        print("[fw init] AGENTS.md wiki block already present")
    else:
        agents.write_text((text.rstrip() + "\n\n" if text else "") + INIT_AGENTS, encoding="utf-8")
        print("[fw init] AGENTS.md wiki-protocol block appended (own block only — rest untouched)")
    (root_dir / "signals").mkdir(exist_ok=True)
    print("[fw init] done — drop .md files into section dirs, then: fw.py sync --write")


def main():
    args = sys.argv[1:]
    cmd = args[0] if args else "sync"
    write = "--write" in args
    if cmd == "init":
        cmd_init(Path.cwd())
        return
    root = wiki_root()
    if cmd == "sync":
        sys.exit(regenerate(root, write))
    elif cmd == "heal":
        sys.exit(regenerate(root, write, heal=True))
    elif cmd == "lint":
        sys.exit(cmd_lint(root))
    elif cmd == "doctor":
        sys.exit(cmd_doctor(root))
    elif cmd == "check":
        sys.exit(cmd_check(root))
    else:
        sys.exit(f"fw: unknown subcommand {cmd!r} (sync|lint|heal|doctor|check)")


if __name__ == "__main__":
    main()
